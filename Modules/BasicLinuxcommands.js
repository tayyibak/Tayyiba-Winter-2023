/**
    Basic Linux commands:

    1. pwd	-> Present working Directory

    2. ls -alrt	-> to view the content of folder/directory
    (Any file/directory name starts with a dot ; means it's hidden)

    3. cd -> Change Directory
    to go onto a folder/directory

    4. mkdir -> Make Directory
    to create a folder in local

    5. rmdir -> Remove Directory
    to delete an empty folder in local

    6. rm -rf -> Remove directory forcefully
    to delete a folder in local

 */